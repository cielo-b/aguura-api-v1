const express = require("express");
const validate = require("../../middlewares/validate");
const authValidation = require("../../validations/auth.validation");
const authController = require("../../controllers/auth.controller");
const auth = require("../../middlewares/auth");

const router = express.Router();

router.post("/register", authController.register);
router.post("/add-customer", authController.addUser);
router.patch("/set-fcm-token", auth(), authController.setFCMToken);
router.post(
  "/connect-ebm",
  auth(["producer", "distributor", "admin"]),
  authController.initializeEBM,
);
router.get("/customers", authController.allCutomers);
router.post("/login", validate(authValidation.login), authController.login);
router.post("/logout", validate(authValidation.logout), authController.logout);
router.get("/profile", auth(""), authController.getUser);
router.post(
  "/refresh-tokens",
  validate(authValidation.refreshTokens),
  authController.refreshTokens,
);
router.post(
  "/forgot-password",
  validate(authValidation.forgotPassword),
  authController.forgotPassword,
);
router.post(
  "/reset-password",
  validate(authValidation.resetPassword),
  authController.resetPassword,
);

module.exports = router;

